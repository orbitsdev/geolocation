class Event {
  final String id;
  final String title;
  final String description;
  final String date;
  final double latitude;
  final double longitude;
  final double radius;

  Event({
    required this.id,
    required this.title,
    required this.description,
    required this.date,
    required this.latitude,
    required this.longitude,
    required this.radius,
  });
}
